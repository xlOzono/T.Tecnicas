// Validations
function validateEmail(email) {
    var re = /\S+@\S+\.\S+/;
    return re.test(email);
}

function validatePhoneNumber(phoneNumber) {
    var re = /^\d{9,15}$/; // 9 to 15 digits
    return re.test(phoneNumber);
}

function validateRUT(rut) {
    var re = /^\d{1,8}-[kK\d]$/;
    return re.test(rut);
}

function validatePassword(password) {
    return password.length >= 6; // Minimum 6 characters
}

function validateLoginForm() {
    var email = document.getElementById("loginEmail").value;
    var password = document.getElementById("loginPassword").value;

    if (!validateEmail(email)) {
        alert("Por favor, ingresa un correo electrónico válido.");
        return false;
    }
    if (!validatePassword(password)) {
        alert("La contraseña debe tener al menos 6 caracteres.");
        return false;
    }
    return true;
}

function validateRegisterForm() {
    var fullName = document.getElementById("registerFullName").value;
    var email = document.getElementById("registerEmail").value;
    var rut = document.getElementById("registerRUT").value;
    var phoneNumber = document.getElementById("registerPhoneNumber").value;
    var password = document.getElementById("registerPassword").value;

    if (fullName.trim() === "") {
        alert("Por favor, ingresa tu nombre completo.");
        return false;
    }
    if (!validateEmail(email)) {
        alert("Por favor, ingresa un correo electrónico válido.");
        return false;
    }
    if (!validateRUT(rut)) {
        alert("Por favor, ingresa un RUT válido.");
        return false;
    }
    if (!validatePhoneNumber(phoneNumber)) {
        alert("Por favor, ingresa un número de teléfono válido.");
        return false;
    }
    if (!validatePassword(password)) {
        alert("La contraseña debe tener al menos 6 caracteres.");
        return false;
    }
    return true;
}
